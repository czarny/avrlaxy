static uint8_t _font_byte(char ch, char i) {
    uint16_t index = (ch - 32) * 5 + i;
    char byte = pgm_read_byte(&(font[index]));
    return byte;
}


void ssd1306_init(void) {
    i2c_init(400);

    ssd1306_command(SSD1306_SETDISPLAYOFF);
    ssd1306_command(SSD1306_SETDISPLAYCLOCKDIVRATIO);
    ssd1306_command(0x80);
    ssd1306_command(SSD1306_SETMULTIPLEXRATIO);
    ssd1306_command(0x3F);
    ssd1306_command(SSD1306_SETDISPLAYOFFSET);
    ssd1306_command(0x00);
    ssd1306_command(SSD1306_SETDISPLAYSTARTLINE | 0x00);

    // We use internal charge pump
    ssd1306_command(SSD1306_SETCHARGEPUMP);
    ssd1306_command(0x14);

    // Horizontal memory mode
    ssd1306_command(SSD1306_SETMEMORYADDRESSINGMODE);
    ssd1306_command(0x00);
    ssd1306_command(SSD1306_SETSEGMENTREMAP | 0x1);
    ssd1306_command(SSD1306_SETCOMOUTPUTSCANDIRECTIONDEC);
    ssd1306_command(SSD1306_SETCOMPINSHWCONFIGURATION);
    ssd1306_command(0x12);

    // Max contrast
    ssd1306_command(SSD1306_SETCONTRASTCONTROL);
    ssd1306_command(0xCF);
    ssd1306_command(SSD1306_SETPRECHARGEPERIOD);
    ssd1306_command(0xF1);
    ssd1306_command(SSD1306_SETVCOMDESELECTLEVEL);
    ssd1306_command(0x40);
    ssd1306_command(SSD1306_ENTIREDISPLAYALLONRESUME);
    ssd1306_command(SSD1306_SETNORMALDISPLAY);
    ssd1306_command(SSD1306_SETDISPLAYON);
}


void ssd1306_command(SSD1306_COMMAND c) {
    i2c_start(SSD1306_ADDRESS);
    i2c_write(0x00);
    i2c_write(c);
    i2c_stop();
}


void ssd1306_data(uint8_t d) {
    i2c_start(SSD1306_ADDRESS);
    i2c_write(0x40);
    i2c_write(d);
    i2c_write(d);
    i2c_stop();
}


void ssd1306_clear(void) {
    ssd1306_command(SSD1306_COLUMNADDRES);
    ssd1306_command(0);
    ssd1306_command(SSD1306_LCDWIDTH - 1); // Column end address (127 = reset)

    ssd1306_command(SSD1306_PAGEADDRES);
    ssd1306_command(0); // Page start address (0 = reset)
    ssd1306_command(7);

    i2c_start(SSD1306_ADDRESS);
    i2c_write(0x40);
    for(int i =0; i < 1024; i++ ) {
        i2c_write(0);
    }
    i2c_stop();
}


void ssd1306_goto(uint8_t x, uint8_t y) {
    ssd1306_command(SSD1306_COLUMNADDRES);
    ssd1306_command(x);
    ssd1306_command(SSD1306_LCDWIDTH - 1); // Column end address (127 = reset)

    ssd1306_command(SSD1306_PAGEADDRES);
    ssd1306_command(y); // Page start address (0 = reset)
    ssd1306_command(7);
}



void ssd1306_putc(char ch) {
    i2c_start(SSD1306_ADDRESS);
    i2c_write(0x40);

    for(char i = 0; i < 5; i++) {
        uint8_t byte = _font_byte(ch, i);
        i2c_write(byte);
    }
    i2c_write(0x00);
    i2c_stop();
}


void ssd1306_puts(char *string) {
    char *b = string;
    while(*b) {
        ssd1306_putc(*b++);
    }
}

